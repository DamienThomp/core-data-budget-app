import SwiftUI

extension Color {

    static var magenta: Self {
        Color(hue: 0.89, saturation: 0.80, brightness: 1)
    }

    static var lightMagenta: Self {
        Color(hue: 0.89, saturation: 0.26, brightness: 1)
    }

    static var peach: Self {
        Color(hue: 0.95, saturation: 0.69, brightness: 1)
    }

    static var darkTeal: Self {
        Color(hue: 0.57, saturation: 0.98, brightness: 0.5)
    }

    static var lightTeal: Self {
        Color(hue: 0.57, saturation: 0.26, brightness: 1)
    }
}
